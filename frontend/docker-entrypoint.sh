#!/bin/bash
set -e

# Install dependencies if node_modules is missing or empty
if [ ! -d "node_modules" ] || [ -z "$(ls -A node_modules)" ]; then
    echo "Installing dependencies..."
    bun install
    # Ensure optional dependencies (like rollup platform binaries) are installed
    # npm handles optional dependencies better than bun in Docker environments
    # npm install --no-save || true
fi

# Execute the command passed to the entrypoint
exec "$@"

